	<p>Listing of the abortion drugs overshadowed the addition of cancer treatments and an anti-stroke medication which were also named by Ms Plibersek as newcomers to the PBS.</p><p>Breakthrough skin cancer drug ipilimumab, late-stage prostate cancer treatment abiraterone and breast cancer medication vinorelbine were added to the list in a move that will cost taxpayers $430 million over the next four years.</p><p>""The exciting thing about these new treatments is that they will extend the life of patients with melanoma, with prostate cancer, with breast cancer,"" Ms Plibersek said.</p><p>""If we didn't subsidise these medicines through the PBS they would be out of the reach of most Australians.""</p><p>The Prostate Cancer Foundation of Australia welcomed news of abiraterone's listing to help in the fight against a cancer that kills 3300 men each year.</p><p>""Affordable access to therapy at all stages of the condition is vital and the minister's decision to include the therapy on the PBS highlights her commitment to all Australians touched by prostate cancer,"" foundation CEO Anthony Lowe said in a statement.</p><p>Cancer Australia also welcomed the new PBS listings.</p><p>""All these agents are important in their own way and to have them available more widely is very important to patients,"" Professor Jim Bishop said on behalf of the national body.</p><p>The government will also put up $450 million over the coming four years for the PBS inclusion of anti-stroke medication rivaroxaban.</p><p>""It's a drug that will be used by patients who have been on Warfarin in the past,"" Ms Plibersek said.</p><p>The revised PBS listings are effective from August.</p>
