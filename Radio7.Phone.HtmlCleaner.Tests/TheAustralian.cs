				<p><strong>
				<!-- google_ad_section_start(name=story_introduction, weight=high) -->
				THE inclusion of controversial abortion drug RU486 on Australia's subsidised medication program has been labelled an ""abuse of power"" by pro-life supporters.
				<!-- google_ad_section_end(name=story_introduction) -->
				</strong></p>
			</div><!-- // .story-intro -->
		<!-- google_ad_section_start(name=story_body, weight=high) -->
		<p>Federal Health Minister Tanya Plibersek announced on Sunday that Mifepristone and Misoprostol - used in combination to terminate a pregnancy of up to seven weeks gestation - would be added to the Pharmaceutical Benefits Scheme (PBS).</p><p>""By offering this different option at a very difficult time in a woman's life, I hope that we are giving more choice in what are often extraordinary difficult circumstances,"" the minister said.</p><p>However Right To Life president Margaret Tighe said the decision was a ""gross abuse of power"" by the government.</p><p>""The PBS is designed to include life-saving drugs,"" she told AAP.</p><p>""In this case these drugs are just going to make it easier and cheaper and quicker to have more abortions in Australia and that's an absolute disgrace and goes against the purpose of the PBS.""</p>
		
